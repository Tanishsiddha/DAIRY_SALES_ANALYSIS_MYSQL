ORDER BY Year, Total_Sold DESC;

-- 19 Products that need immediate restock --
SELECT Product_Name, Quantity_in_Stock, Min_Stock_Threshold
FROM dairy_data
WHERE Quantity_in_Stock <= Min_Stock_Threshold;

-- 20 Identify expired products still in stock --
SELECT Product_Name, Expiration_Date, Quantity_in_Stock
FROM dairy_data
WHERE Expiration_Date < curdate()
AND Quantity_in_Stock > 0;

-- 21 Average shelf life per brand --
SELECT Brand, AVG(Shelf_Life) AS Avg_Shelf_Life
FROM dairy_data
GROUP BY Brand;

-- 22 Most popular product in each customer location --
SELECT Customer_Location, Product_Name, SUM(Quantity_Sold) AS Total_Sold
FROM dairy_data
GROUP BY Customer_Location, Product_Name
ORDER BY Customer_Location, Total_Sold DESC;









